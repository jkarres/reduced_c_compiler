import java.util.Vector;


abstract class Scope
{
	//----------------------------------------------------------------
	//
	//----------------------------------------------------------------
	public Scope()
	{
		locals = new Vector();
	}


	//----------------------------------------------------------------
	//
	//----------------------------------------------------------------
	public STO access(String strName)
	{
		return accessLocal(strName);
	}


	//----------------------------------------------------------------
	//
	//----------------------------------------------------------------
	public STO accessLocal(String name)
	{
        for (STO sto : locals)
            if (sto.getName().equals(name))
                return sto;

		return null;
	}


	//----------------------------------------------------------------
	//
	//----------------------------------------------------------------
	public void InsertLocal(STO sto)
	{
		locals.addElement(sto);
	}

    public String toString() {
        String retval = "<Scope>";
        for (STO sto : locals)
            retval += sto.toString();
        retval += "</Scope>";
        return retval;
    }


    //----------------------------------------------------------------
    //	Instance variables.
    //----------------------------------------------------------------
	protected Vector<STO> locals;
}
